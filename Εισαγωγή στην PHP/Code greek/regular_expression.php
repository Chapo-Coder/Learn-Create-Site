    u - Επιτρέπει τη σωστή αντιστοίχιση των κωδικοποιημένων μοτίβων UTF-8

    Patterns - Μοτίβα:
    [abc] - Βρείτε έναν χαρακτήρα από τις επιλογές μεταξύ των αγκυλών
