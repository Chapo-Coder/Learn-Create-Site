    preg_match()  - Επιστρέφει 1 εάν το μοτίβο βρέθηκε στη συμβολοσειρά και 0 εάν όχι
    preg_match_all() - Επιστρέφει τον αριθμό των φορών που το μοτίβο βρέθηκε στη συμβολοσειρά, η οποία μπορεί επίσης να είναι 0
    preg_replace() - Επιστρέφει μια νέα συμβολοσειρά όπου τα αντιστοιχισμένα μοτίβα έχουν αντικατασταθεί με μια άλλη συμβολοσειρά

